// Our very first function

// Two step process:
// 1. Define
function funcName() {
  // do something
}

// Example of a function
function grumpus() {
  console.log("ugh... you again");
  console.log("for the last time");
  console.log("LEAVE ME ALONE!");
}

// Another function example
function sing() {
  console.log("DO");
  console.log("RE");
  console.log("MI");
}

// 2. Run
sing();

// It's important to call the function, JavaScript will not run
// it until we tell him to do so.

sing(); // run it again!

// Remember to define your functions first, and then run them.
