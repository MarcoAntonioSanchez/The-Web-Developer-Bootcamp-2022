// For loop examples

// 1st example
// Printing out even numbers from 1 to 20
for (let i = 0; i <= 20; i += 2) {
  console.log(i);
}

// 2nd example
// Printing out odd numbers
for (let i = 1; i <= 20; i += 2) {
  console.log(i);
}

// 3rd example
// Counting down from 100 to 0
for (let i = 100; i >= 0; i -= 10) {
  console.log(i);
}

// 4th example
// Incrementing 10 by 10 to 100
for (let i = 10; i <= 100; i *= 10) {
  console.log(i);
}
